n = int(input())
dp = [0 for _ in range(2*n+1)]
dp[1] = 1
dp[2] = 3

for i in range(2, 2*n+1):
    dp[i] = max(dp[i-1] + dp[i-2]*2, dp[i])

print(dp[n]%10007)
